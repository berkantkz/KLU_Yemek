public class KLU_List {
    private String day;
    private String content;
    private String date;

    KLU_List() {

    }

    String getDay() {
        return day;
    }

    void setDay(String day) {
        this.day = day;
    }

    String getContent() {
        return content;
    }

    void setContent(String content) {
        this.content = content;
    }

    String getDate() {
        return date;
    }

    void setDate(String date) {
        this.date = date;
    }

}
